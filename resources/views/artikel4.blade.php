<p>Hasil setiap balapan dihitung menggunakan sebuah sistem poin untuk menentukan dua gelar juara dunia: satu untuk pembalap, dan satu lagi untuk konstruktor.
    Setiap balapan harus mempunyai Super Licence, kelas izin balapan paling tinggi yang diberikan oleh FIA. Balapan-balapan F1 harus diselenggarakan di
    sirkuit berperingkat "1" (dulu "A"), yang merupakan peringkat sirkuit tertinggi dalam sistem peringkat FIA</p>
<p>Mobil Formula Satu adalah mobil balap tercepat di dunia, karena kecepatan menikung yang tinggi yang dihasilkan oleh aerodinamika gaya turun yang besar.
    Sebagian besar gaya turun tersebut berasal dari sayap depan dan belakang, yang memiliki efek samping, yaitu turbulensi di belakang mobil. Turbulensi
    tersebut mengurangi gaya turun yang dapat dihasilkan oleh mobil yang mengikuti di belakang, dan membuat penyalipan sukar dilakukan. Pada musim 2022,
    terdapat perubahan besar pada regulasi mobil yang menggunakan lebih banyak aerodinamika efek tanah, dan sayap mobil yang diubah untuk mengurangi turbulensi
    di belakang mobil. Ini bertujuan untuk membuat penyalipan lebih mudah dilakukan. Sistem kontrol traksi, transmisi otomatis dan sistem bantuan kemudi
    elektronik lainnya pertama kali dilarang digunakan dalam Formula Satu pada 1994. Sistem-sistem tersebut diperkenalkan kembali pada 2001, dan dilarang lagi sejak 2008</p>
<p>Pada 23 Januari 2017, Liberty Media menyelesaikan akuisisi Formula One Group, dari CVC Capital Partners sebesar $8 miliar</p>
<h3 style="font-weight: 400;">Sejarah</h3>
<hr>
<p>Seri Formula Satu berakar dari seri Kejuaraan Eropa dariGrand Prix mobil pada sekitar 1920-an dan 1930-an. Sejumlah organisasi balap grand prix membuat sejumlah aturan untuk
    kejuaraan dunia sebelum Perang Dunia II. Dengan alasan penundaan karena perang, kejuaraan dunia pembalap tidak diformalkan sampai 1947 dan berlangsung untuk pertama kalinya
    pada 1950. Kejuaraan dunia konstruktor kemudian menyusul pada 1958. Balapan Formula Satu tanpa gelar diselenggarakan bertahun-tahun, tetapi dikarenakan membengkaknya biaya
    kompetisi mengakibatkan kompetisi ini berakhir pada awal 1980-an.</p>
