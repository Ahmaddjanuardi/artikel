<h2>Formula Satu</h2>
<hr>
<p>Formula Satu, disingkat F1 (atau bernama lengkap FIA Formula One World Championship), adalah kelas tertinggi balap mobil kursi tunggal yang diatur oleh
    Federasi Otomotif Internasional (FIA) dan dimiliki oleh Formula One Group. Kata formula di "Formula Satu" mengacu pada peraturan dan regulasi yang harus
    diikuti semua peserta. Formula Satu terdiri dari sejumlah seri balapan yang dikenal dengan istilah Grand Prix. Balapan-balapan tersebut diselenggarakan
    baik dalam sirkuit yang dibangun khusus atau jalan raya tertutup.</p>
