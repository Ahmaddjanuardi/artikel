<p>Gelar juara dunia Formula Satu pertama kali dimenangkan oleh pembalap asal Italia Giuseppe Farina dengan mobilnya Alfa Romeo pada tahun 1950, dengan mengalahkan rekan setimnya,
    pembalap Argentina Juan Manuel Fangio. Akan tetapi, Fangio memenangkan gelar juara dunia pada tahun 1951 dan empat kali pada enam tahun berikutnya. Fangio kemudian menjadi legenda
    yang mendominasi tahun-tahun pertama kompetisi Formula Satu.</p>

<p>Juara dunia dari Britania Raya yang pertama adalah Mike Hawthorn, yang mengendarai Ferrari memenangkan gelar juara pada 1958. Kemudian Colin Chapman memasuki kompetisi F1 sebagai
    perancang mobil dan kemudian menjadi pendiri Lotus, British racing green datang untuk mendominasi kompetisi pada beberapa dekade berikutnya. Jim Clark, Jackie Stewart, Jack Brabham,
    Graham Hill, dan Denny Hulme adalah sederetan nama pembalap dari tim Inggris dan negara-negara persemakmuran yang memenangkan dua belas gelar juara dunia antara tahun 1962 hingga 1973.</p>
