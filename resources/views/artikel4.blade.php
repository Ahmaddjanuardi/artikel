<p>Tahun 1962, Lotus memperkenalkan mobil dengan rangka aluminium yang dikenal dengan istilah monocoque yang menggantikan rangka tubular tradisional. Penemuan ini kemudian menjadi langkah kemajuan
    teknologi besar sejak penemuan mobil bermesin tengah. Tahun 1968 adalah tahun pertama kalinya olahraga ini memakai sponsor ditandai dengan Lotus yang mengecat "Imperial Tobacco" di mobilnya.
    Aerodinamika gaya turun (downforce) secara perlahan memainkan peranan penting dalam perancangan mobil, dimulai dengan munculnya aerofoil tahun 1960-an. Akhir tahun 1970, Lotus memperkenalkan
    aerodinamika efek tanah (ground effect) yang menghasilkan gaya tekan yang bagus sehingga meningkatkan kecepatan di tikungan (konsep ini pernah diuji coba sebelumnya oleh Jim Hall dengan tim
    IndyCar-nya pada tahun 1960-an.</p>
<h4>Kepopuleran</h4>
<p>Tahun 1981 dikenal sebagai awal dari adanya Perjanian Concorde (Concorde Agreement), sebuah kontrak yang mengikat tim-tim untuk berkompetisi sampai masa berakhirnya kontrak. Kontrak itu juga
    berisi tentang pembagian sama rata atas keuntungan yang didapat dari hasil penjualan hak televisi. Ini juga merupakan tanda berakhirnya Perang FISA-FOCA. Perjanjian Concorde yang kedua
    ditandatangani pada 1992 dan yang ketiga pada 1997 di mana akan berakhir pada akhir 2007.</p>
<p>Tim F1 Renault memperkenalkan mesin turbocharged pada tahun 1977 yang dapat menghasilkan lebih dari 700 bhp. Pada tahun-tahun berikutnya, khususnya 1987, mobil-mobil Formula Satu dapat
    menghasilkan lebih dari 1.000 bhp. FIA kemudian memberlakukan aturan kapasitas tanki bahan bakar pada 1984 dan kemudian melarang mesin turbocharged tahun 1989 untuk membatasi kecepatan mobil yang semakin meningkat.</p>
<p>Awal 1990 ditandai dengan diperkenalkannya alat bantu elektronik seperti power steering, traction control dan gearbox semi otomatis. FIA, dikarenakan banyaknya komplain mengenai hasil
    balapan yang lebih ditentukan oleh teknologi dibandingkan keahlian pembalap, melarang beberapa alat bantu tersebut pada 1994. Walaupun demikian banyak pengamat berpendapat bahwa larangan
    atas alat bantu pembalap tersebut tidak berarti sama sekali karena FIA tidak mempunyai teknologi atau metode untuk menghilangkan fitur-fitur tersebut dari kompetisi.</p>
<p>Tim McLaren dan Williams mendominasi balapan 1980-an dan 1990-an. Honda dan McLaren mendominasi banyak pada 1980-an, sementara tim Williams yang diperkuat mesin Renault memenangi beberapa gelar juara dunia pertengahan
    1990-an. McLaren kemudian kembali pada akhir 1990-an. Pertarungan antara legenda Senna dan Prost menjadi pusat perhatian pada musim 1988 dan berlanjut sampai akhir 1993 di mana Prost menyatakan untuk pensiun. Roland
    Ratzenberger secara tragis tewas pada tabrakan di kualifikasi Grand Prix F1 San Marino 1994 dan Ayrton Senna secara tragis tewas pada tabrakan di lomba Grand Prix F1 San Marino 1994. Sejak itu banyak langkah diambil
    FIA untuk meningkatkan standar keamanan. Tidak ada pembalap yang tewas di balapan sejak saat itu sampai Jules Bianchi tewas pada tabrakan di lomba Grand Prix F1 Jepang 2014 dan kematian Bianchi pada tahun berikutnya.</p>
<p>Pembalap dari McLaren, Williams, Renault (dulunya bernama Benetton) dan Ferarri merupakan empat tim teratas yang memenangi gelar juara dunia dari 1984 sampai saat ini. Karena kemajuan teknologi sejak 1990-an, biaya kompetisi
    \Formula Satu juga meningkat. Kedua hal ini mengakibatkan tim-tim lainnya mengalami kesulitan tidak hanya untuk bertahan dalam kompetisi namun untuk bertahan dalam bisnis. Masalah keuangan mengakibatkan beberapa tim untuk
    mundur. Sejak 1990, 28 tim telah mundur dari kompetisi Formula Satu. Salah satunya yang terjadi akhir-akhir ini adalah mundurnya tim Jordan.</p>
<h4>F1 Modern</h4>
<p>Banyak rekor telah dipecahkan pada kompetisi pada abad ke-21, khususnya di tangan Michael Schumacher, Fernando Alonso, Lewis Hamilton, dan Sebastian Vettel. Awal tahun 2000 merupakan tahun dominasi Michael Schumacher dan
    tim Ferarri. Pada tahun 2001, Schumacher memecahkan rekor untuk kemenangan terbanyak; rekor sebelumnya dipegang oleh Alain Prost, dengan 51 kemenangan. Pada tahun 2002, Schumacher mencatat rekor klaim juara paling awal
    dengan menjuarai Grand Prix F1 Prancis 2002 pada bulan Juli tahun itu. Tahun 2003, Schumacher mengklaim gelar juara dunianya yang ke-6, mengalahkan pemegang rekor sebelumnya Juan Manuel Fangio yang memegang gelar juara
    dunia lima kali. Rekornya sekarang adalah 7 gelar juara dunia. Tahun 2008 Sebastian Vettel menjadi pembalap termuda yang menempati posisi pertama (pole position) saat memimpin babak kualifikasi pada Grand Prix F1 Italia
    2008, dan pada besoknya ia menjadi juara balapan termuda dalam sejarah.</p>
